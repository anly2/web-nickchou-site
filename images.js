default_category = "poster";
default_offset   =  0;

_uri = false;
function category(){
   var cat;

   var h = window.location.href;
   var r = h.match(/(\?|&)(.*?)(&|$)/);
   _uri = (r==null)? "" : r[2];

   var r = _uri.match(/^(.*?)(:|$)/);

   if(typeof images[r[1]] == 'undefined')
      return default_category;

   cat = r[1];
   return cat;
}

function offset(){
   if(_uri===false){
      category();
      return offset();
   }

   var r = _uri.match(/:(\d)/i);

   if(r == null)
      return default_offset;

   return r[1];
}